    public void lerTabela() throws SQLException, ClassNotFoundException{
        DefaultTableModel modelo = (DefaultTableModel) tabelaClientes.getModel();
        modelo.setNumRows(0);
        ClienteDAO dao = new ClienteDAO();
        
        for(Cliente dado: dao.listarClientes()){
            modelo.addRow(new Object[]{
            dado.getId(),
            dado.getNome(),
            dado.getCPF(),
            dado.getEmail(),
            dado.getTelefone()
            });
        }
    }
    
    public void lerTabelaBusca(String nome) throws SQLException, ClassNotFoundException{
        DefaultTableModel modelo = (DefaultTableModel) tabelaClientes.getModel();
        modelo.setNumRows(0);
        ClienteDAO dao = new ClienteDAO();
        
        for(Cliente dado: dao.listarClientePorNome(nome)){
                modelo.addRow(new Object[]{
                dado.getId(),
                dado.getNome(),
                dado.getCPF(),
                dado.getEmail(),
                dado.getTelefone()
            });
        }
    }
