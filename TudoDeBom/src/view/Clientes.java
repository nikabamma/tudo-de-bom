    private void excluirActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {//GEN-FIRST:event_excluirActionPerformed
        // TODO add your handling code here:
        if(tabelaClientes.getSelectedRow() != -1){
            Cliente dado = new Cliente();
            ClienteDAO dao = new ClienteDAO(); 
            dado.setId((int)(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 0)));

            dao.excluirCliente(dado);

            lerTabela();
        }
    }//GEN-LAST:event_excluirActionPerformed

    private void adicionarActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {//GEN-FIRST:event_adicionarActionPerformed
        Cliente dado = new Cliente();
        ClienteDAO dao = new ClienteDAO(); 
        
        dado.setNome(nomeCliente.getText());
        dado.setCPF(cpfCliente.getText());
        dado.setEmail(emailCliente.getText());
        dado.setTelefone(telefoneCliente.getText());
        
        dao.inserirCliente(dado);
        
        lerTabela();
    }//GEN-LAST:event_adicionarActionPerformed

    private void alterarActionPerformed(java.awt.event.ActionEvent evt) throws ClassNotFoundException, SQLException {//GEN-FIRST:event_alterarActionPerformed
        
        
        if(tabelaClientes.getSelectedRow() != -1){
            Cliente dado = new Cliente();
            ClienteDAO dao = new ClienteDAO(); 

            dado.setNome(nomeCliente.getText());
            dado.setCPF(cpfCliente.getText());
            dado.setEmail(emailCliente.getText());
            dado.setTelefone(telefoneCliente.getText());
            dado.setId((int)(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 0)));

            dao.alterarCliente(dado);

            lerTabela();
        }
    }//GEN-LAST:event_alterarActionPerformed

    private void tabelaClientesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabelaClientesKeyReleased
        if(tabelaClientes.getSelectedRow() != -1){
            
            nomeCliente.setText((tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 1)).toString());
            cpfCliente.setText((tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 2)).toString());
            emailCliente.setText((tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 3)).toString());
            telefoneCliente.setText((tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 4)).toString());
        }
    }//GEN-LAST:event_tabelaClientesKeyReleased

